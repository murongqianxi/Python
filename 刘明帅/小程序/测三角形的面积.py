print('按q退出\n')
while True:
    def abc_efg(C, Y):
        abc = C * Y / 2
        return abc


    poi = input('三角形的高为(厘米):')
    if poi == 'q':
        break
    poi = int(poi)
    yui = input('三角形的底为(厘米):')
    if yui == 'q':
        break
    yui = int(yui)
    c2f = abc_efg(poi, yui)
    print('三角'
          '形的面积是:', c2f, '平方厘米\n')
